class Solution
{
public:
    vector<int> singleNumber(vector<int> nums) 
    {
        // Code here.
        int x = 0, a = 0, b = 0;
        for (int& k : nums)
            x ^= k;
        x = x & ~(x - 1);
        for (int& k : nums) {
            if (k & x) a ^= k;
            else b ^= k;
        }
        if (a < b) return {a, b};
        return {b, a};
    }
};

